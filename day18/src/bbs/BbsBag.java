package bbs;

public class BbsBag {
	String id;
	String title;
	String content;
	String writer;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	

	
	
	//getters and setters  라고 부른다 너주는애들~갖고오는애들~ 
	//가방에 넣을떄는 set 메소드를 사용하고
	//가방에서 꺼내올때는 get메소드
	
	
	
	
}
